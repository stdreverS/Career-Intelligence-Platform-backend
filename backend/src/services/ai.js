// Сохраняем только суть разговора, не все сообщения
// ============================================
async function compressContext(messages, existingSummary) {
  if (messages.length < 6) return null; // сжимаем только если накопилось

  const summaryPrompt = `Ты — система сжатия контекста. 
Создай краткое резюме диалога в 2-3 предложениях на русском языке.
Сохрани только ключевые факты о пользователе: имя, навыки, опыт, желаемая позиция.
Не добавляй ничего лишнего.

${existingSummary ? `Предыдущий контекст: ${existingSummary}` : ''}

Диалог:
${messages.slice(0, -4).map(m => `${m.role}: ${m.content}`).join('\n')}`;

  try {
    const response = await groq.chat.completions.create({
      model: 'llama-3.3-70b-versatile',
      messages: [{ role: 'user', content: summaryPrompt }],
      max_tokens: 200,
      temperature: 0.3
    });
    return response.choices[0].message.content;
  } catch {
    return existingSummary; // если не удалось — оставляем старый
  }
}

