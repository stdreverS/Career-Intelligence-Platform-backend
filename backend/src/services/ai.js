// ВАЛИДАЦИЯ СООБЩЕНИЙ
// Проверяем сообщение ДО отправки в ИИ
// ============================================
const BLOCKED_PATTERNS = [
  // Попытки сменить роль
  /забудь (все |свои )?(инструкции|правила|роль)/i,
  /ты теперь/i,
  /представь что ты/i,
  /притворись что ты/i,
  /ты больше не/i,
  /ignore (previous|all|your) (instructions|rules)/i,
  /you are now/i,
  /act as/i,
  /roleplay as/i,
  /pretend (you are|to be)/i,
  /jailbreak/i,
  /dan mode/i,
  /developer mode/i,
  // Попытки вытащить системный промпт
  /покажи (свой|твой)? (промпт|инструкци|систем)/i,
  /what (is|are) your (instructions|system prompt|rules)/i,
  /reveal your (system|instructions)/i,
];

const OFF_TOPIC_PATTERNS = [
  /напиши (стихи|стихотворение|рассказ|сказку|шутку)/i,
  /реши (задачу|уравнение|пример|загадку)/i,
  /переведи (на|с) [а-яёa-z]/i,
  /напиши (код|программу|скрипт|функцию)/i,
  /объясни (квантов|физик|математик|химию|историю)/i,
  /кто (такой|такая|придумал|изобрёл)/i,
  /что такое (?!резюме|карьера|навык|должность|стажировка|портфолио|linkedin|hh\.ru)/i,
  /расскажи (анекдот|историю|сказку)/i,
  /сыграй|поиграем/i,
  /погода|курс (валют|доллар|евро)/i,
];

function validateMessage(content) {
  // Проверяем на попытки взлома промпта
  for (const pattern of BLOCKED_PATTERNS) {
    if (pattern.test(content)) {
      return {
        valid: false,
        reason: 'inject',
        response: 'Я работаю только как карьерный консультант и не могу менять свою роль. Давайте продолжим работу над вашим резюме!'
      };
    }
  }

  // Проверяем на явный офтопик
  for (const pattern of OFF_TOPIC_PATTERNS) {
    if (pattern.test(content)) {
      return {
        valid: false,
        reason: 'offtopic',
        response: 'Я специализируюсь только на создании резюме и карьерных консультациях. Давайте вернёмся к работе над вашим резюме!'
      };
    }
  }

  return { valid: true };
}

