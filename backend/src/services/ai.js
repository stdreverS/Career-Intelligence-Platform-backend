// ГЛАВНАЯ ФУНКЦИЯ — отправка сообщения
// ============================================
async function sendMessage(messages, contextSummary) {
  // Если есть сжатый контекст — используем его вместо старых сообщений
  let contextualMessages;

  if (contextSummary && messages.length > 6) {
    // Берём только последние 4 сообщения + добавляем summary как контекст
    const recentMessages = messages.slice(-4);
    contextualMessages = [
      {
        role: 'user',
        content: `[КОНТЕКСТ ПРЕДЫДУЩЕГО ДИАЛОГА]: ${contextSummary}`
      },
      {
        role: 'assistant',
        content: 'Понял, продолжаю работу с этим контекстом.'
      },
      ...recentMessages
    ];
  } else {
    contextualMessages = messages;
  }

  const response = await groq.chat.completions.create({
    model: 'llama-3.3-70b-versatile',
    messages: [
      { role: 'system', content: SYSTEM_PROMPT },
      ...contextualMessages.map(m => ({
        role: m.role === 'assistant' ? 'assistant' : 'user',
        content: m.content
      }))
    ],
    max_tokens: 2000,
    temperature: 0.7
  });

  return response.choices[0].message.content;
}

function extractResumeFromResponse(text) {
  try {
    const jsonMatch = text.match(/```json\n?([\s\S]*?)\n?```/);
    if (jsonMatch) {
      return JSON.parse(jsonMatch[1]);
    }
    return null;
  } catch {
    return null;
  }
}

